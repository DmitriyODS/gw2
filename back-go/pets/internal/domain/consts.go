// TierFor — текущий уровень по заработанному + следующий (nil на максимуме).
func TierFor(earned int) (BankTier, *BankTier) {
	current := BankTiers[0]
	for i := range BankTiers {
		if earned >= BankTiers[i].Threshold {
			current = BankTiers[i]
		} else {
			next := BankTiers[i]
			return current, &next
		}
	}
	return current, nil
}


// Package service — ИИ техподдержки: автоответчик dev-чата мессенджера.
// Отвечает сам на простые вопросы «как пользоваться платформой», может
// поддержать беседу; всё сложное (баги, данные, аккаунты, пожелания)
// эскалирует разработчикам готовой фразой — супер-админы видят весь dev-чат
// и отвечают вручную. Ключ — ПЛАТФОРМЕННЫЙ (env SUPPORT_AI_API_KEY), не
// компанийный: владелец dev-чата может вообще не состоять в компании.
package service

import (
	"context"
	"encoding/json"
	"strings"
	"time"

	"github.com/DmitriyODS/gw2/back-go/ai/internal/domain"
)

const (
	supportMaxTokens   = 500
	supportTemperature = 0.4
	supportTimeout     = 45 * time.Second
	// supportDefaultModel — если SUPPORT_AI_MODEL не задан.
	supportDefaultModel = "gpt-4o-mini"
)

// SupportConfig — платформенный LLM техподдержки (из env aisvc).
type SupportConfig struct {
	APIKey string
	Model  string
}

// supportEscalationText — канон фразы эскалации; она же зашита в промпт.
const supportEscalationText = "Мы передали ваш вопрос разработчикам — они обязательно вернутся с ответом здесь, в этом чате."

func supportSystemPrompt() string {
	return `Ты — ИИ-помощник техподдержки платформы Groove Work. Отвечаешь пользователям в чате «Техподдержка»; этот же чат читают разработчики платформы и могут вмешаться в любой момент.

О платформе (отвечай только в рамках этих знаний, не выдумывай функции):
Groove Work — платформа учёта рабочего времени, задач и коллаборации.
- Задачи и юниты: юнит — отрезок работы над задачей с таймером, единовременно активен только один; статистика по сотрудникам/отделам/типам работ, теги и личные цвета задач, ТВ-режим со слайдами показателей.
- Компании и роли: любой пользователь может создать компанию и стать её администратором; приглашения — ссылкой или письмом; роли — Сотрудник, Менеджер, Администратор; один аккаунт может состоять в нескольких компаниях и переключаться между ними; можно вообще не состоять в компании — тогда доступны мессенджер, звонки, заметки, ежедневники, профиль.
- Мессенджер: личные чаты, реакции-эмодзи, ответы и пересылка, закрепление, вложения до 25 МБ, Markdown-разметка, статус пользователя (эмодзи + текст).
- Звонки: аудио/видео до 9 участников, демонстрация экрана, ссылки-приглашения для внешних гостей.
- Заметки: rich-текст, группы, закрепление, публичные ссылки (просмотр/правка), совместное редактирование, экспорт/импорт txt, ИИ-инструменты текста (нужна компания с включённым ИИ).
- Ежедневники: личные списки дел по дням, архив выполненного, перенос записей, связь с задачами, шаринг ссылкой или конкретному человеку.
- Реестры и календари компании: настраиваемые таблицы-справочники и календари событий с полями разных типов, экспорт в Excel, внешние ссылки на просмотр.
- Корпоративный портал: посты с комментариями и реакциями, тематические разделы, закрепление, пересылка постов в чат.
- Питомцы-грувики (если включены в компании): виртуальный питомец, кудосы за работу, магазин, банк кудосов (переводы, вклады, копилки, сборы), приключения, рейтинг.
- ИИ-ассистент по задачам и статистике: вкладка «Ассистент» в плавающем окне чатов; включает администратор компании в настройках компании (нужен ключ ИИ).
- Приложения: для компьютера (Windows/macOS/Linux) и Android — скачать в настройках, карточка «О приложении»; веб-версия работает в браузере.
- Аккаунт: регистрация с подтверждением email, восстановление пароля по почте, смена пароля в профиле.

Правила:
1. Отвечай кратко, дружелюбно и по-русски. Поздороваться и немного поболтать можно, но мягко возвращай разговор к платформе.
2. На простые вопросы «как сделать / где найти / что это» отвечай сам по знаниям выше.
3. Если сообщение — об ошибке или сбое, проблеме с данными, аккаунтом или доступом, просьба что-то починить/изменить/добавить, жалоба, либо ты не уверен в ответе — НЕ фантазируй, а ответь фразой: «` + supportEscalationText + `» (допустима короткая вежливая обвязка вокруг неё).
4. Никаких технических подробностей внутреннего устройства платформы, никаких обещаний сроков, не отвечай от имени конкретных людей.`
}

// SupportReply — один ход LLM над историей dev-чата (messages_json — реплики
// user/assistant БЕЗ системного промпта, его добавляем здесь). Ключ не задан →
// AI_DISABLED: msgsvc откатывается на канированный автоответ.
func (s *Service) SupportReply(ctx context.Context, messagesJSON string) (string, error) {
	if s.support.APIKey == "" {
		return "", errAiDisabled(409)
	}
	var history []json.RawMessage
	if err := json.Unmarshal([]byte(messagesJSON), &history); err != nil || len(history) == 0 {
		return "", domain.NewError("AI_BAD_REQUEST", "messages_json — невалидный JSON-массив", 400)
	}
	system, _ := json.Marshal(map[string]string{"role": "system", "content": supportSystemPrompt()})
	messages, err := json.Marshal(append([]json.RawMessage{system}, history...))
	if err != nil {
		return "", err
	}
	model := s.support.Model
	if model == "" {
		model = supportDefaultModel
	}
	res, err := s.llm.ChatOnce(ctx, domain.ChatParams{
		APIKey:       s.support.APIKey,
		Model:        model,
		MessagesJSON: string(messages),
		MaxTokens:    supportMaxTokens,
		Temperature:  supportTemperature,
		Timeout:      supportTimeout,
	})
	if err != nil {
		return "", err
	}
	content := strings.TrimSpace(res.Content)
	if content == "" {
		return "", domain.NewError("AI_EMPTY", "Пустой ответ модели", 502)
	}
	return content, nil
}
